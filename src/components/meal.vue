<template>
  <div>
    <h1 class="title">Random Food</h1>
    <div>
          <img v-if="!isWorking" :src="meal.image" alt="" />
          <div v-if="isWorking" class="loader-con">
               <div class="loader" >Loading</div>
          </div>
           
    </div>
  
    <button @click="getAMeal">Get a meal</button>
  <router-link to="/">Go back to home</router-link>
  </div>
</template>
<script>
import Vue from "vue";
export default {
  name: "dogFacts",
  data() {
    return {
      meal: {},
      isWorking: false,
    };
  },
  methods: {
    getAMeal: function () {
      this.isWorking = true;
      let that = this
      Vue.axios
        .get("https://foodish-api.herokuapp.com/api")
        .then((response) => {
            setTimeout(function(){ 
                 that.isWorking = false;
                 that.meal = response.data;
            }, 1000);
         
        });
    },
  },
  mounted() {
    this.getAMeal();
  },
};
</script>
